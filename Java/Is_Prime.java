import java.util.Scanner;
public class Prime {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int n = sc.nextInt();
    boolean IsPrime = true;
    for(int i = 2; i*i<=n; i++){
      if ( n % i ==0 ) {
        IsPrime = false;
        break;
      }
    }
  if(n<2) IsPrime = false;

  System.out.println("Is Prime : "+ IsPrime);
  }
}
